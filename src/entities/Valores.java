package entities;

public class Valores {
	
	public int quantiTabuaCadeira, quantiTabuaMesa, quantiTempoCadeira, quantiTempoMesa, totalDeTabuas, totalDeHoras;
	
	public Valores() {
	}

	public Valores(int quantiTabuaCadeira, int quantiTabuaMesa, int quantiTempoCadeira, int quantiTempoMesa,  int totalDeTabuas, int totalDeHoras) {
		this.quantiTabuaCadeira = quantiTabuaCadeira;
		this.quantiTabuaMesa = quantiTabuaMesa;
		this.quantiTempoCadeira = quantiTempoCadeira;
		this.quantiTempoMesa = quantiTempoMesa;
		this.totalDeTabuas = totalDeTabuas;
		this.totalDeHoras = totalDeHoras;
	}
	
	
}
